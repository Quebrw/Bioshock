package src.tools;

public class Vector2f {

    private float xpos;
    private float ypos; 

    public Vector2f(){
        this.xpos = 0f;
        this.ypos = 0f;
    }

    public float getXpos() {
        return xpos;
    }

    public float getYpos() {
        return ypos;
    }

    public void setXpos(float xpos) {
        this.xpos = xpos;
    }

    public void setYpos(float ypos) {
        this.ypos = ypos;
    }

}
